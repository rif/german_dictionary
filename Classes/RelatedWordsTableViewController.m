#pragma mark -
#pragma mark Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
	NSString *searchedWord = [self.word.relatedWords objectAtIndex: indexPath.row];
	NSArray *words = [[RootViewController getSections] valueForKey: [[searchedWord substringToIndex:1] uppercaseString]];
	for (Word *w in words) {
		if ([w.word isEqualToString: searchedWord]) {
			DefinitionViewController *dvc = [[DefinitionViewController alloc] initWithNibName:@"DefinitionViewController" bundle:nil];
			[((DoxaAppDelegate *)[UIApplication sharedApplication].delegate).navigationController pushViewController:dvc animated:YES];
			
			dvc.title = w.word;
			dvc.definitionView.text = w.definition;
			dvc.relatedWordsTableViewController.word = w;
			[(UITableView*)dvc.relatedWordsTableViewController.view reloadData];
			[dvc release];
			return;
		}
	}
	UITableViewCell *cell = [tableView cellForRowAtIndexPath: indexPath];
	[cell setSelectionStyle:UITableViewCellSelectionStyleGray];
	//NSLog(@"%@ not found!", searchedWord);
}


